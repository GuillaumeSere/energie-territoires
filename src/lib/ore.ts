export function listTerritories(): TerritorySummary[] {
  return territories.map(({ code, name, department, region, population, consumptionMwh, evolutionPercent }) => ({
    code,
    name,
    department,
    region,
    population,
    consumptionMwh,
    evolutionPercent,
  }));
}

export async function getTerritory(code: string): Promise<Territory | null> {
  const territory = territories.find((item) => item.code === code);

  if (territory) {
    return territory;
  }

  const commune = await getCommuneByCode(code);

  return commune ? makeEstimatedTerritory(commune) : null;
}

export function compareTerritories(codes: string[]) {
  const wanted = new Set(codes);
  return territories.filter((territory) => wanted.has(territory.code));
}
