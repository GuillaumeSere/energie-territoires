import type { DataCenterImpact, DpeBreakdown, EnergySector, Territory, TerritorySummary } from "./types";
import { getCommuneByCode, makeEstimatedTerritory } from "./communes";

type TerritorySeed = Omit<Territory, "history" | "sectors" | "dpe" | "dataCenterImpact"> & {
  sectorShares: Record<EnergySector, number>;
  dpeShares: DpeBreakdown["share"][];
  dataCenterMwh: number;
  gridPressure: DataCenterImpact["gridPressure"];
  heatReusePotential: DataCenterImpact["heatReusePotential"];
  waterStress: DataCenterImpact["waterStress"];
  renewableContractShare: number;
  pueTarget: number;
};

const dpeLabels: DpeBreakdown["label"][] = ["A", "B", "C", "D", "E", "F", "G"];

function makeHistory(consumptionMwh: number, evolutionPercent: number, emissionsTco2e: number) {
  const base = Math.round(consumptionMwh / (1 + evolutionPercent / 100));
  const emissionBase = Math.round(emissionsTco2e / (1 + evolutionPercent / 120));
  const steps = [1, 0.94, 0.985, 0.965, 0.945, consumptionMwh / base];

  return steps.map((factor, index) => ({
    year: 2019 + index,
    consumptionMwh: Math.round(base * factor),
    emissionsTco2e: Math.round(emissionBase * (factor - index * 0.006)),
  }));
}

function makeTerritory(seed: TerritorySeed): Territory {
  const dataCenterShare = Number(((seed.dataCenterMwh / seed.consumptionMwh) * 100).toFixed(1));

  return {
    ...seed,
    history: makeHistory(seed.consumptionMwh, seed.evolutionPercent, seed.emissionsTco2e),
    sectors: Object.entries(seed.sectorShares).map(([sector, share]) => ({
      sector: sector as EnergySector,
      share,
      consumptionMwh: Math.round((seed.consumptionMwh * share) / 100),
    })),
    dpe: dpeLabels.map((label, index) => ({ label, share: seed.dpeShares[index] })),
    dataCenterImpact: {
      estimatedAnnualMwh: seed.dataCenterMwh,
      shareOfTerritoryConsumption: dataCenterShare,
      gridPressure: seed.gridPressure,
      heatReusePotential: seed.heatReusePotential,
      waterStress: seed.waterStress,
      renewableContractShare: seed.renewableContractShare,
      pueTarget: seed.pueTarget,
      notes: [
        "Estimation de démonstration à remplacer par des données opérateurs, raccordement RTE/Enedis et bilans environnementaux.",
        "Suivre la puissance appelée, l'origine de l'électricité, le PUE, le refroidissement et la récupération de chaleur.",
        "Croiser les nouveaux projets avec la disponibilité réseau et les besoins locaux de chaleur bas carbone.",
      ],
    },
  };
}

